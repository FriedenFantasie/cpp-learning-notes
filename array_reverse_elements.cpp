#include <iostream> // 引入输入输出头文件，用来使用 cout 和 endl

using namespace std; // 使用标准命名空间，后面可以直接写 cout

int main() // 主函数，程序从这里开始执行
{ // main 函数开始
    int arr[5] = {1, 2, 3, 4, 5}; // 定义一个长度为 5 的一维数组，并给它赋初始值
    int start = 0; // 定义左边下标，数组第一个元素的下标是 0
    int end = 4; // 定义右边下标，数组最后一个元素的下标是 4

    cout << "数组逆置前："; // 输出提示文字，表示接下来显示逆置前的数组
    for (int i = 0; i < 5; i++) { // 使用 for 循环遍历数组，i 从 0 到 4
        cout << arr[i] << " "; // 输出当前下标 i 对应的数组元素
    } // for 循环结束，数组所有元素输出完毕
    cout << endl; // 输出换行，让后面的内容显示到下一行

    while (start < end) { // 当左边下标小于右边下标时，继续交换元素
        int temp = arr[start]; // 用临时变量 temp 保存左边元素，防止交换时数据丢失
        arr[start] = arr[end]; // 把右边元素赋值给左边位置
        arr[end] = temp; // 把临时变量中保存的左边元素赋值给右边位置

        start++; // 左边下标向右移动一位
        end--; // 右边下标向左移动一位
    } // while 循环结束，说明所有需要交换的元素都已经交换完

    cout << "数组逆置后："; // 输出提示文字，表示接下来显示逆置后的数组
    for (int i = 0; i < 5; i++) { // 再次遍历数组，查看逆置后的结果
        cout << arr[i] << " "; // 输出当前数组元素
    } // for 循环结束，逆置后的数组输出完毕
    cout << endl; // 输出换行

    return 0; // 返回 0，表示程序正常结束
} // main 函数结束
